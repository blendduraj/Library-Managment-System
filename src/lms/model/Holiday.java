
package lms.model;


public class Holiday {

    private int id;
    private String holiday;
    private String date;

   
    public int getId() {
        return id;
    }


    public void setId(int id) {
        this.id = id;
    }


    public String getHoliday() {
        return holiday;
    }

 
    public void setHoliday(String holiday) {
        this.holiday = holiday;
    }


    public String getDate() {
        return date;
    }


    public void setDate(String date) {
        this.date = date;
    }
}
